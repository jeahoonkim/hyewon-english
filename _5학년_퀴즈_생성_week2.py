        ('20260515', templates['english'], '혜원이_영어일상회화_퀴즈_20260515.html',  ENGLISH_5_15),
        ('20260516', templates['nonsense'],'혜원이_넌센스퀴즈_6탄_20260516.html',     NONSENSE_5_16),
